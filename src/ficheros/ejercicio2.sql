

/* Create Tables */

CREATE TABLE AUTORES
(
	ID_AUTOR number NOT NULL,
	NOMBRE_AUTOR varchar2(100) NOT NULL,
	PRIMARY KEY (ID_AUTOR)
);


CREATE TABLE EXAMENT2.ENCICLOPEDIAS
(
	ID_ENCICLOPEDIA number NOT NULL,
	NOMBRE varchar2(100 char) NOT NULL,
	ISBN varchar2(20 char) NOT NULL,
	EDITORIAL varchar2(75 char) NOT NULL,
	FECHA_PUBLICACION date,
	ID_AUTOR number NOT NULL,
	CONSTRAINT SYS_C007397 PRIMARY KEY (ID_ENCICLOPEDIA)
);


CREATE TABLE EXAMENT2.TOMOS
(
	ID_TOMO number NOT NULL,
	NOMBRE_TOMO varchar2(50 char) NOT NULL,
	ID_ENCICLOPEDIA number NOT NULL,
	CONSTRAINT SYS_C007400 PRIMARY KEY (ID_TOMO)
);



/* Create Foreign Keys */

ALTER TABLE EXAMENT2.ENCICLOPEDIAS
	ADD FOREIGN KEY (ID_AUTOR)
	REFERENCES AUTORES (ID_AUTOR)
;


ALTER TABLE EXAMENT2.TOMOS
	ADD CONSTRAINT TOMOS_ENCICLOPEDIAS_FK FOREIGN KEY (ID_ENCICLOPEDIA)
	REFERENCES EXAMENT2.ENCICLOPEDIAS (ID_ENCICLOPEDIA)
;



